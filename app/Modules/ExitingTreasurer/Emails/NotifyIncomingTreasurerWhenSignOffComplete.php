<?php

namespace App\Modules\ExitingTreasurer\Emails;

use Illuminate\Bus\Queueable;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;
use Illuminate\Contracts\Queue\ShouldQueue;

class NotifyIncomingTreasurerWhenSignOffComplete extends Mailable implements ShouldQueue
{
    use Queueable, SerializesModels;

    public $user;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct($user)
    {
        $this->user = $user;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this
            ->subject('Financial Account Sign Off Complete')
            ->markdown('exitingtreasurer::emails.incoming_treasurer_when_exiting_treasurer_signed_off');
    }
}
